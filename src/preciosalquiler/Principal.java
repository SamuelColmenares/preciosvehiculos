    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if (txtPlaca.getText().trim().equals("")
                || txtModelo.getText().trim().equals("")
                || txtFechaIn.getText().trim().equals("")
                || TxtFechaOut.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Deben llenarse todos los campos!");
            return;
        }

        modeloTabla = (DefaultTableModel) tblCarros.getModel();
        Vehiculo vehiculo;
        String tipoCarro = cbTipoCarro.getSelectedItem().toString();

        try {
            switch (tipoCarro) {
                case "Camion":
                    Camion camion = new Camion();
                    camion.setPesoMax(Integer.parseInt(txtPeso.getText()));
                    vehiculo = camion;
                    break;
                case "Furgoneta":
                    Furgoneta furgoneta = new Furgoneta();
                    furgoneta.setPesoMax(Integer.parseInt(txtPeso.getText()));
                    vehiculo = furgoneta;
                    break;
                case "Microbus":
                    vehiculo = new Microbus();
                    break;
                default:
                    vehiculo = new Coche();
                    break;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Valores de peso máximo debe ser numérico!");
            return;
        }

        vehiculo.setPlaca(txtPlaca.getText());
        vehiculo.setModelo(txtModelo.getText());
        vehiculo.setMarca(cbMarca.getSelectedItem().toString());
        String fechaIngreso = txtFechaIn.getText(),
                fechaSalida = TxtFechaOut.getText();

        try {
            vehiculo.setFechaEntrega(new SimpleDateFormat("d/MM/yyyy").parse(fechaIngreso));
            vehiculo.setFechaSalida(new SimpleDateFormat("d/MM/yyyy").parse(fechaSalida));
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(null, "Valores de fecha inválido!");
            return;
        }

        modeloTabla.addRow(adicionarVehiculoARow(vehiculo));
        tblCarros.setModel(modeloTabla);
        this.vehiculos.add(vehiculo);
        this.limpiarCampos();
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnCalculoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalculoActionPerformed
        String placa = txtPlacaCalc.getText().trim();
        if (placa.equals("")) {
            JOptionPane.showMessageDialog(null, "Debe llenarse el campo de placa a calcular!");
            return;
        }

        if (this.vehiculos.size() < 1) {
            JOptionPane.showMessageDialog(null, "No hay vehiculos adicionados. \nAdicione vehículo primero.");
            return;
        }

        Vehiculo busqueda;

        try {
            busqueda = this.vehiculos.stream()
                    .filter(v -> v.getPlaca().equals(placa))
                    .findFirst()
                    .orElseThrow();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "No hay vehiculos con esta placa.");
            return;
        }

        lblCantDias.setText("" + busqueda.cantDiasAlquiler());
        lblPagar.setText("" + busqueda.calcularPrecio());

    }//GEN-LAST:event_btnCalculoActionPerformed

