    /**
     * Convierte el vehiculo a row de la tabla
     * @param vehiculo vehiculo a convertir
     * @return Arreglo de objetos listo para adicionar a tabla
     */
    private Object[] adicionarVehiculoARow(Vehiculo vehiculo) {
        Object[] row = new Object[7];
        String tipo = "Coche";
        int pesoMax = 0;

        if (vehiculo instanceof Microbus) {
            tipo = "Microbus";
        } else if (vehiculo instanceof Furgoneta) {
            tipo = "Furgoneta";
            pesoMax = ((Furgoneta) vehiculo).getPesoMax();
        } else if (vehiculo instanceof Camion) {
            tipo = "Camion";
            pesoMax = ((Camion) vehiculo).getPesoMax();
        }

        row[0] = vehiculo.getPlaca();
        row[1] = vehiculo.getModelo();
        row[2] = tipo;
        row[3] = vehiculo.getMarca();
        row[4] = pesoMax;
        row[5] = vehiculo.getFechaEntrega();
        row[6] = vehiculo.getFechaSalida();
        return row;
    }

